def multiply(a: int, b: int) -> int:
    """
    乘法工具函数
    这是一个简单的数学计算工具，用于演示工具调用机制

    Args:
        a: 第一个整数
        b: 第二个整数

    Returns:
        int: 两个整数的乘积
    """
    return a * b
